#7. Create an array of people that are invited to a party.
#Allow the user to ask if a certain person is invited to the party.
#If the person is on the invitation list respond INVITED otherwise respond NOT INVITED.
lists = ["Jess", "Dave", "Peter", "Chris", "Kevin", "Lucy"]
print "Please enter your name: "
name = gets.chomp.to_s
in_list = true
lists.each do |list|
  if name == list
    in_list = true
    break
  else
    in_list = false
  end
end

if in_list
  puts "INVITED"
else
  puts "NOT INVITED"
end

#8.Have the user enter in a sentence. Then, using an array, store the frequency of each letter of the alphabet from the sentence.
# Print out the frequency of each letter. Do not count uppercase and lowercase letters differently.
print "Enter a sentence: "
sentence = gets.chomp
letters = sentence.upcase.split("")
letters.uniq.each do |elem|
  puts "#{elem}: #{letters.count(elem)}"
end
